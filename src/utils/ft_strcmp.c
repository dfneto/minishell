#include <errno.h>
#include <libft.h>
#include <minishell.h>
#include <stdlib.h>
#include <unistd.h>

int	ft_strcmp(char *str1, char *str2)
{
	while (*str1 && *str2 && *str1 == *str2)
	{
		str1++;
		str2++;
	}
	return (*str1 - *str2);
}
